async function generateBatch(plan) {
  const userMsg = `Сгенерируй ${plan.count} карточек категории "${plan.category}" (${plan.desc}).
Уровень интенсивности: ${plan.level} (0-3).
ID начинай с "${plan.category}_001" и далее.
Верни одним JSON-массивом из ${plan.count} элементов.`;

  console.log(`\n🤖 ${plan.category} (${plan.count} карточек)...`);

  const res = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: {
      "x-api-key": ANTHROPIC_KEY,
      "anthropic-version": "2023-06-01",
      "content-type": "application/json",
    },
    body: JSON.stringify({
      model: MODEL,
      max_tokens: 8000,
      system: SYSTEM,
      messages: [{ role: "user", content: userMsg }],
    }),
  });

  if (!res.ok) {
    console.error(`❌ ${res.status}:`, await res.text());
    return [];
  }

  const data = await res.json();
  const text = data.content?.[0]?.text || "";
  const cleaned = text.replace(/^```json\s*/i, "").replace(/```\s*$/, "").trim();

  try {
    const cards = JSON.parse(cleaned);
    console.log(`   ✅ ${cards.length} карточек | tokens in=${data.usage.input_tokens}, out=${data.usage.output_tokens}`);
    return cards;
  } catch (e) {
    console.error(`   ❌ Parse error:`, e.message);
    console.error(text.slice(0, 500));
    return [];
  }
}

